u16 radio_rando[countof(radio_vanilla)];

void radio_randomize() {
  memset(radio_rando, 0xFF, sizeof(radio_rando));
  int max = countof(radio_vanilla);
  if (ap_save.options[AP_OPTION_RADIO_RANDO] != AP_OPTION_RADIO_RANDO_ON_INCLUDING_TRAINING) {
    max -= 33;
    for (int i = max; i < countof(radio_vanilla); i++) radio_rando[i] = i;
  }
  int iter = max;
  while (iter) {
    int pos = crand()%iter--;
    for (int i = 0; i < max; i++) {
      if (radio_rando[i] == 0xFFFF && !pos--) {
        radio_rando[i] = iter;
        break;
      }
    }
  }
}

void radio_play(u32 msg, sf_radio_character_t character) {
  int iter = 0;
  if (ap_save.options[AP_OPTION_RADIO_RANDO]) {
    for (; iter < countof(radio_vanilla); iter++) {
      if (radio_vanilla[iter].ptr == msg) goto found_msg;
    }
  }
  sf_fn_play_radio_message((u16*)msg, character);
  return;
  found_msg:
  radio_msg_t* r = &radio_vanilla[radio_rando[iter]];
  sf_team_id_t team = TEAM_ID_MAX;
  int shields = 0;
  switch (r->character) {
    case RCID_PEPPY:
      team = TEAM_ID_PEPPY;
      break;
    case RCID_SLIPPY:
      team = TEAM_ID_SLIPPY;
      break;
    case RCID_FALCO:
      team = TEAM_ID_FALCO;
      break;
    default: goto skip_shield_check;
  }
  shields = sf_team_shields[team];
  sf_team_shields[team] = 1;
  skip_shield_check:
  sf_fn_play_radio_message((u16*)r->ptr, character);
  if (team < TEAM_ID_MAX) sf_team_shields[team] = shields;
}
